print("\n=== نموذج إجابة Q1 ===")
class Student:
    def __init__(self, name, student_id, gpa=0.0):
        self.name = name
        self.student_id = student_id
        self.gpa = gpa
    
    def display(self):
        print(f"الطالب: {self.name}, الرقم الجامعي: {self.student_id}, المعدل: {self.gpa}")

student1 = Student("أحمد", 2021001, 3.75)
student2 = Student("سارة", 2021002, 3.9)
student1.display()
student2.display()