# Q14: حل معادلة من الدرجة الثانية (مثل التمرين 2)
# ----------------------------------------------------------
# اطلب إدخال a,b,c ثم استخدم math.sqrt لحساب Δ وطباعة الحلول
# (أضف import math إذا لزم الأمر)

# --- أكتب الكود هنا ---

import math
a = float(input("a = "))
b = float(input("b = "))
c = float(input("c = "))
delta = b*b - 4*a*c

if delta < 0:
    print("لا حلول حقيقية")
elif delta == 0:
    x = -b / (2*a)
    print("حل واحد: x =", x)
else:
    x1 = (-b - math.sqrt(delta)) / (2*a)
    x2 = (-b + math.sqrt(delta)) / (2*a)
    print("حلّان: x1 =", x1, "x2 =", x2)
    
