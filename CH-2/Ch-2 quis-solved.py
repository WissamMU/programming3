# أنشئ أول 5 صفوف من مثلث باسكال

n = 5
pascal = [[1]]  # الصف الأول

for i in range(1, n):
    row = [1]
    for j in range(1, i):
        row.append(pascal[i-1][j-1] + pascal[i-1][j])
    row.append(1)
    pascal.append(row)

for row in pascal:
    print(row)
# [1]
# [1, 1]
# [1, 2, 1]
# [1, 3, 3, 1]
# [1, 4, 6, 4, 1]

