# Q15: إنشاء مصفوفة باستخدام list comprehension
# ----------------------------------------------------------
# أنشئ مصفوفة 3x3 تحتوي على جدول الضرب (عناصرها i*j)
# ثم اطبع المصفوفة بشكل منظم

