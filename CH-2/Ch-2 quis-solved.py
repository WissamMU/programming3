# Q12: فرز قائمة باستخدام Bubble Sort
# ----------------------------------------------------------
# طبق خوارزمية الفرز الفقاعي على القائمة: [5, 2, 8, 1, 9]
# ثم اطبع القائمة بعد الفرز

Bubble = [5, 2, 8, 1, 9]
n = len(Bubble)
for i in range(n):
    for j in range(0 , n - i - 1):
        if Bubble[j] > Bubble[j+1]:
            Bubble[j] , Bubble[j+1] = Bubble[j+1] , Bubble[j]

print(Bubble)
print("=" * 50)

