# Q14: نظام إدارة الدرجات (مثال متقدم)
# ----------------------------------------------------------
# أنشئ قائمة تحتوي على 3 طلاب، كل طالب: [id, اسم, درجة_امتحان, درجة_أعمال]
# احسب المحصلة (70% امتحان + 30% أعمال) وحدد النجاح/الرسوب

Students = [
    [12436, "Sami Ahmad", 80.8, 90.5],
    [12440, "Anas Mansour", 50.4, 80.5],
    [13822, "Moustafa Zein", 57.2, 70]
]

for student in Students:
    id_num, name, exam, practical = student
    total = exam * 0.70 + practical * 0.30
    success = (exam >= 40) and (practical >= 40) and (total >= 60)
    status = "Success" if success else "Fail"
    print(f"{name} ({id_num}): {total:.1f} → {status}")

print("=" * 50)

